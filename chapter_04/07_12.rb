# 以下のコードはa = []と同じ
a = Array.new

# ----------------------------------------

# 要素が5つの配列を作成する
a = Array.new(5)
a #=> [nil, nil, nil, nil, nil]

# ----------------------------------------

# 要素が5つで0をデフォルト値とする配列を作成する
a = Array.new(5, 0)
a #=> [0, 0, 0, 0, 0]

# ----------------------------------------

# 1, 2, 3, 1, 2, 3...と繰り返す配列を作る
a = Array.new(10) { |n| n % 3 + 1 }
a #=> [1, 2, 3, 1, 2, 3, 1, 2, 3, 1]