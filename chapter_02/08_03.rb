# ヒアドキュメントの識別子としてHTMLを使う場合
<<HTML
<div>
  <img src="sample.jpg">
</div>
HTML

# ----------------------------------------

def some_method
  <<-TEXT
これはヒアドキュメントです。
<<-を使うと最後の識別子をインデントさせることができます。
  TEXT
end

puts some_method
#=> これはヒアドキュメントです。
#   <<-を使うと最後の識別子をインデントさせることができます。

# ----------------------------------------

def some_method
  <<~TEXT
    これはヒアドキュメントです。
    <<~を使うと内部文字列のインデント部分が無視されます。
  TEXT
end

puts some_method
#=> これはヒアドキュメントです。
#   <<~を使うと内部文字列のインデント部分が無視されます。

# ----------------------------------------

def some_method
  <<~TEXT
    \  各行の行頭に半角スペースを2文字を入れます。
    \  このとき行頭はバックスラッシュで指定します。
  TEXT
end

puts some_method
#=>   各行の行頭に半角スペースを2文字入れます。
#     このとき行頭はバックスラッシュで指定します。

